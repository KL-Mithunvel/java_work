package Operations;

public class AssignmentOperator {
    public void equalto(int a,int b){
        a=b;
    }
public void addwith(int a,int b){
    a+=b;
}
    public void subwith(int a,int b){
        a-=b;
    }
    public void prodwith(int a,int b){
        a*=b;
    }
    public void divwith(int a,int b){
        a/=b;
    }

}
